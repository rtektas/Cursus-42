#include "../../include/libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;
	int	j;

	i = 0;
	if (!f || !s)
		return ;
	j = (int)ft_strlen(s);
	if (s != NULL && f != NULL)
	{
		while (i < j && s != NULL && f != NULL)
		{
			(*f)(i, s);
			i++;
			s++;
		}
	}
}
